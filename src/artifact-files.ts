function writeLogo(project: ArtifactProject, logoDir: string): string {
  const dataUrl = text(project.details.logoDataUrl, '');
  if (!dataUrl.startsWith('data:image/')) return '';
  const match = dataUrl.match(/^data:(image\/[a-zA-Z0-9.+-]+);base64,(.+)$/);
  if (!match) return '';
  const mimeType = match[1] ?? 'image/png';
  const payload = match[2] ?? '';
  const extension = logoExtension(text(project.details.logoFileName, ''), mimeType);
  mkdirSync(logoDir, { recursive: true });
  const fileName = `logo.${extension}`;
  writeFileSync(join(logoDir, fileName), Buffer.from(payload, 'base64'));
  return `../../public/brand/${fileName}`;
}


const ARTIFACT_ROOT = '/opt';
const REQUIRED_ARTIFACTS = ['proposal', 'specs', 'design', 'tasks', 'verification', 'archive'];

export function writeProjectArtifacts(project: ArtifactProject) {
  const projectDir = join(ARTIFACT_ROOT, project.slug);
  resetGeneratedArtifacts(projectDir);
  const capability = safeSegment(text(project.details.capabilityName, project.slug), project.slug);
  const changeId = safeSegment(text(project.details.changeId, `add-${project.slug}`), `add-${project.slug}`);
  const specDir = join(projectDir, 'openspec', 'specs', capability);
  const changeDir = join(projectDir, 'openspec', 'changes', changeId);
  const changeSpecDir = join(changeDir, 'specs', capability);

  mkdirSync(specDir, { recursive: true });
  mkdirSync(changeSpecDir, { recursive: true });
  mkdirSync(join(projectDir, '.vscode'), { recursive: true });
  mkdirSync(join(projectDir, 'docs'), { recursive: true });

  const artifacts = new Map(project.artifacts.map((artifact) => [artifact.key, artifact.content]));
  for (const key of REQUIRED_ARTIFACTS) {
    if (!artifacts.get(key)?.trim()) {
      throw new Error(`Cannot write project artifacts: missing generated ${key} content`);
    }
  }
  writeFileSync(join(projectDir, 'README.md'), renderReadme(project, changeId), 'utf8');
  writeFileSync(join(projectDir, 'AGENTS.md'), renderAgentInstructions(project), 'utf8');
  writeFileSync(join(projectDir, '.vscode', 'settings.json'), renderVSCodeSettings(), 'utf8');
  writeFileSync(join(projectDir, '.vscode', 'extensions.json'), renderVSCodeExtensions(), 'utf8');
  writeFileSync(join(projectDir, 'openspec', 'config.yaml'), renderOpenSpecConfig(project), 'utf8');
  writeFileSync(join(projectDir, 'openspec', 'project.md'), renderProjectConventions(project), 'utf8');
  writeFileSync(join(specDir, 'spec.md'), artifacts.get('specs') ?? '', 'utf8');
  writeFileSync(join(changeDir, 'proposal.md'), artifacts.get('proposal') ?? '', 'utf8');
  writeFileSync(join(changeDir, 'design.md'), artifacts.get('design') ?? '', 'utf8');
  writeFileSync(join(changeDir, 'tasks.md'), artifacts.get('tasks') ?? '', 'utf8');
  writeFileSync(join(changeSpecDir, 'spec.md'), artifacts.get('specs') ?? '', 'utf8');
  writeFileSync(join(projectDir, 'docs', 'ai-sanity-check.md'), renderSanityCheckProtocol(project), 'utf8');
  writeFileSync(join(projectDir, 'docs', 'builder-inputs.md'), renderBuilderInputs(project), 'utf8');
  writeFileSync(join(projectDir, 'docs', 'verification.md'), artifacts.get('verification') ?? '', 'utf8');
  writeFileSync(join(projectDir, 'docs', 'archive.md'), artifacts.get('archive') ?? '', 'utf8');
  writeFileSync(join(projectDir, 'project.json'), JSON.stringify(project, null, 2), 'utf8');
  const logoHref = writeLogo(project, join(projectDir, 'public', 'brand'));
  writeWireframe(project, join(projectDir, 'docs', 'wireframe'), logoHref);
}

export function deleteProjectArtifacts(slug: string) {
  rmSync(join(ARTIFACT_ROOT, slug), { recursive: true, force: true });
}

function resetGeneratedArtifacts(projectDir: string) {
  rmSync(join(projectDir, 'openspec'), { recursive: true, force: true });
  rmSync(join(projectDir, 'docs', 'wireframe'), { recursive: true, force: true });
  rmSync(join(projectDir, 'public', 'brand'), { recursive: true, force: true });
}


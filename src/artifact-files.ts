function detectAppType(project: ArtifactProject) {
  const source = `${project.name} ${project.description} ${text(project.details.mainWorkflow, '')} ${text(project.details.dataNeeds, '')}`;
  if (/\bfactoring\b|\binvoice factoring\b/i.test(source)) return 'invoice_factoring';
  return 'general_workflow';
}

function deriveNavItems(details: Record<string, unknown>, isRiteFact: boolean) {
  const source = `${text(details.mainWorkflow, '')} ${text(details.mission, '')}`;
  const preferred = isRiteFact ? ['Home', 'Apply', 'Documents', 'Invoices', 'Status', 'Settings'] : ['Home', 'Workflow', 'Records', 'Status', 'Settings'];
  if (isRiteFact) return preferred;
  const found = preferred.filter((item) => new RegExp(`\\b${escapeRegExp(item)}\\b`, 'i').test(source));
  return found.length >= 4 ? found : preferred;
}

function deriveEntities(details: Record<string, unknown>) {
  const raw = text(details.dataNeeds, '');
  const entities = raw
    .split(/[,;\n]+/)
    .map((item) => item.replace(/\([^)]*\)/g, '').trim())
    .filter((item) => item.length > 2)
    .slice(0, 8);
  return entities.length ? entities : ['Users', 'Records', 'Files', 'Statuses', 'Settings'];
}

function summarizeWorkflow(details: Record<string, unknown>, isRiteFact: boolean) {
  if (isRiteFact) {
    return [
      { title: 'Onboard company', detail: 'Collect company identity, contact details, and secure profile basics.' },
      { title: 'Complete documents', detail: 'Upload, photograph, OCR, review, and save required application files.' },
      { title: 'Attach invoices', detail: 'Add invoices, validate readiness, then submit factoring packages.' },
      { title: 'Track funding', detail: 'Monitor submitted, follow up, approved, funded, and rejected states.' }
    ];
  }
  const sentences = text(details.mainWorkflow, text(details.userActions, 'User completes the primary workflow and reviews the result.'))
    .split(/\n+|(?<=\.)\s+/)
    .map((line) => line.replace(/^\d+\.\s*/, '').trim())
    .filter(Boolean)
    .slice(0, 4);
  return (sentences.length ? sentences : ['Open the app', 'Complete the main task', 'Review status', 'Save the result']).map((line, index) => ({
    title: ['Start', 'Capture', 'Review', 'Finish'][index] ?? `Step ${index + 1}`,
    detail: line
  }));
}

function shortLabel(value: string) {
  return value.replace('App Builder', 'Build').replace('Settings', 'More');
}

function normalizeHex(value: string | undefined, fallback: string) {
  if (!value) return fallback;
  const trimmed = value.trim();
  if (/^#[0-9a-fA-F]{6}$/.test(trimmed)) return trimmed.toLowerCase();
  if (/^[0-9a-fA-F]{6}$/.test(trimmed)) return `#${trimmed.toLowerCase()}`;
  return fallback;
}

function escapeCssIdentifier(value: string) {
  return value.replace(/[^a-zA-Z0-9 _-]/g, '').split(',')[0] || 'Inter';
}

function escapeRegExp(value: string) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function logoExtension(fileName: string, mimeType: string) {
  const fromName = fileName.toLowerCase().match(/\.([a-z0-9]+)$/)?.[1];
  if (fromName && ['png', 'jpg', 'jpeg', 'webp', 'svg'].includes(fromName)) return fromName === 'jpg' ? 'jpeg' : fromName;
  if (mimeType.includes('svg')) return 'svg';
  if (mimeType.includes('webp')) return 'webp';
  if (mimeType.includes('jpeg') || mimeType.includes('jpg')) return 'jpeg';
  return 'png';
}

function text(value: unknown, fallback: string) {
  return typeof value === 'string' && value.trim() ? value.trim() : fallback;
}

